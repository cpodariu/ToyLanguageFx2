package Utils;

public class Triplet
{
	String one;
	String two;
	String three;
	
	public Triplet(String one, String two, String three) {
		this.one = one;
		this.two = two;
		this.three = three;
	}
	
	public String getOne() {
		return one;
	}
	
	public String getTwo() {
		return two;
	}
	
	public String getThree() {
		return three;
	}
}
